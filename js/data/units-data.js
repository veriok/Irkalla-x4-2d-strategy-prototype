export const UNITS = UNIT_DEFS.map(_normalizeUnit);

/** Fast lookup by unit id */
export const UNIT_MAP = Object.fromEntries(UNITS.map(u => [u.id, u]));

/** Get non-militia unit types for a faction */
export function getUnitsForFaction(factionId, includeMilitia = false) {
  return UNITS.filter(u => u.factionId === factionId && (includeMilitia || !u.isMilitia));
}

/**
 * Get units recruitable at a location.
 * @param {string}   factionId
 * @param {string[]} existingBuildingIds  installed building ids at this location
 * @param {string|null} locationType      location type (e.g. 'main_settlement', 'fort')
 * @param {string[]} unlockedTechs        faction's unlocked tech ids
 */
export function getRecruitableUnits(factionId, existingBuildingIds, locationType = null, unlockedTechs = []) {
  return getUnitsForFaction(factionId).filter(u => {
    // Tech gate
    if (u.techRequired && !unlockedTechs.includes(u.techRequired)) return false;

    // Building requirement
    const req = u.requiredBuilding;
    if (req === null) {
      const TRAINING_BUILDINGS = ['mustering_field', 'warrior_lodge', 'barracks'];
      return locationType === 'main_settlement' || TRAINING_BUILDINGS.some(id => existingBuildingIds.includes(id));
    }

    if (Array.isArray(req)) return req.some(id => existingBuildingIds.includes(id));
    return existingBuildingIds.includes(req);
  });
}

