/**
 * units-data.js
 *
 * Unit type definitions for all 8 factions + neutral militia.
 *
 * attack:             base attack power per individual unit
 * defense:            base defense power per individual unit
 * maxHp:              hit points per individual soldier
 * cost:               { [resourceId]: amount } — paid on recruitment
 * buildTurns:         turns in production queue (dwarf units are +1 vs equivalents by design)
 * stackSize:          how many units are recruited per queue entry
 * factionId:          which faction can recruit this unit
 * raceId:             racial identity (used by event callbacks, e.g. soul resurrection filter)
 * tier:               combat/economy tier (1..3)
 * movement:           moves per turn (army uses slowest unit)
 * upkeepGold:         gold upkeep paid each turn
 * traitIds:           trait ids from traits-data.js
 * effects:            inline effect definitions (scope/type/condition/timing)
 * isMilitia:          true for province garrison profiles (not recruitable via queue)
 * requiredBuilding:   building id(s) that must exist in the location to recruit
 * techRequired:       tech id that must be unlocked to recruit this unit
 */

